#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	t;

	t = (unsigned char)c;
	if ((t >= 'a') && (t <= 'z'))
	{
		return (1);
	}
	else if ((t >= 'A') && (t <= 'Z'))
	{
		return (1);
	}
	else if ((t <= '9') && (t >= '0'))
	{
		return (1);
	}
	return (0);
}
