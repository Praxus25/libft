#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		n;
	char	*p;

	n = 0;
	p = (NULL);
	while (n <= ft_strlen(s))
	{
		if ((char)c == (((char *)s)[n]))
		{
			p = &((char *)s)[n];
		}
		n++;
	}
	return (p);
}
